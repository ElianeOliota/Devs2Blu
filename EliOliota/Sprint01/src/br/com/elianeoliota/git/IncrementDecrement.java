package br.com.elianeoliota.git;

public class IncrementDecrement {

	public static void main(String[] args) {
		// incrementando
				System.out.print("Incrementando : ");
				for (int vezes = 1; vezes <= 10; vezes++) {
					System.out.print(vezes + " ");
				}
				System.out.println();
				// decrementando
				System.out.print("Decrementando : ");
				for (int vezes = 10; vezes >= 1; vezes--) {
					System.out.print(vezes + " ");
				}

	}

}
